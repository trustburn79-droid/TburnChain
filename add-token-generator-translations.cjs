// Read each locale file and add the tokenGenerator section
const localesDir = path.join(__dirname, 'client/src/locales');

// Helper to read and parse JSON
function readLocale(lang) {
  const filePath = path.join(localesDir, `${lang}.json`);
  const content = fs.readFileSync(filePath, 'utf8');
  return JSON.parse(content);
}

// Helper to write JSON
function writeLocale(lang, data) {
  const filePath = path.join(localesDir, `${lang}.json`);
  fs.writeFileSync(filePath, JSON.stringify(data, null, 2), 'utf8');
  console.log(`Updated ${lang}.json with tokenGenerator translations`);
}

// Process main languages with full translations
const mainLangs = ['en', 'ko', 'ja', 'zh', 'es', 'fr'];
mainLangs.forEach(lang => {
  try {
    const data = readLocale(lang);
    data.tokenGenerator = translations[lang];
    writeLocale(lang, data);
  } catch (err) {
    console.error(`Error processing ${lang}:`, err.message);
  }
});

// Process additional languages using English as fallback
additionalLangs.forEach(lang => {
  try {
    const data = readLocale(lang);
    data.tokenGenerator = translations.en;
    writeLocale(lang, data);
  } catch (err) {
    console.error(`Error processing ${lang}:`, err.message);
  }
});


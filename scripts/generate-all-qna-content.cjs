function translateTags(tags, locale) {
  if (locale === 'ko') return tags;
  const translations = tagTranslations[locale] || tagTranslations.en;
  return tags.map(tag => translations[tag] || tag);
}

function updateLocaleFile(locale) {
  const filePath = path.join(localesDir, `${locale}.json`);
  let data;
  
  try {
    const content = fs.readFileSync(filePath, 'utf8');
    data = JSON.parse(content);
  } catch (e) {
    console.error(`Error reading ${locale}.json:`, e.message);
    return;
  }

  const qnaDataFile = fs.readFileSync(path.join(__dirname, '../client/src/data/qna-data.ts'), 'utf8');
  
  const qnaArrayMatch = qnaDataFile.match(/export const qnaData: QnAItem\[\] = \[([\s\S]*?)\];/);
  if (!qnaArrayMatch) {
    console.error('Could not find qnaData array');
    return;
  }

  const content = {};
  
  for (let i = 1; i <= 100; i++) {
    const idPattern = new RegExp(`id:\\s*${i}\\s*,`, 'm');
    const startMatch = qnaDataFile.search(idPattern);
    if (startMatch === -1) continue;
    
    let endMatch = qnaDataFile.length;
    for (let j = i + 1; j <= 101; j++) {
      const nextPattern = new RegExp(`id:\\s*${j}\\s*,`, 'm');
      const nextMatch = qnaDataFile.search(nextPattern);
      if (nextMatch !== -1 && nextMatch > startMatch) {
        endMatch = nextMatch;
        break;
      }
    }
    
    const itemText = qnaDataFile.substring(startMatch, endMatch);
    
    const questionMatch = itemText.match(/question:\s*['"](.+?)['"]\s*,/);
    const questionEnMatch = itemText.match(/questionEn:\s*['"](.+?)['"]\s*,/);
    const answerMatch = itemText.match(/answer:\s*['"](.+?)['"]\s*,/);
    const answerEnMatch = itemText.match(/answerEn:\s*['"](.+?)['"]\s*,/);
    const tagsMatch = itemText.match(/tags:\s*\[([^\]]+)\]/);
    
    if (!questionMatch || !questionEnMatch || !answerMatch || !answerEnMatch) {
      console.log(`Skipping item ${i} - missing fields`);
      continue;
    }
    
    let question, answer, tags;
    
    if (locale === 'ko') {
      question = questionMatch[1];
      answer = answerMatch[1];
    } else if (locale === 'en') {
      question = questionEnMatch[1];
      answer = answerEnMatch[1];
    } else {
      question = questionEnMatch[1];
      answer = answerEnMatch[1];
    }
    
    let tagsArray = [];
    if (tagsMatch) {
      tagsArray = tagsMatch[1].split(',').map(t => t.trim().replace(/['"]/g, ''));
    }
    tags = translateTags(tagsArray, locale);
    
    content[String(i)] = {
      question,
      answer,
      tags
    };
  }

  if (!data.qna) {
    data.qna = {};
  }
  data.qna.content = content;

  fs.writeFileSync(filePath, JSON.stringify(data, null, 2), 'utf8');
  console.log(`Updated ${locale}.json with ${Object.keys(content).length} Q&A items`);
}

const locales = ['en', 'ko', 'zh', 'ja', 'es', 'fr', 'ru', 'ar', 'hi', 'bn', 'pt', 'ur'];

locales.forEach(locale => {
  updateLocaleFile(locale);
});


function generateContent(lang) {
  const content = {};
  
  qnaData.forEach(item => {
    let question, answer, tags;
    
    if (lang === 'en') {
      question = item.questionEn;
      answer = item.answerEn;
      tags = translateTags(item.tags, 'en');
    } else if (lang === 'ko') {
      question = item.question;
      answer = item.answer;
      tags = item.tags;
    } else {
      question = item.questionEn;
      answer = item.answerEn;
      tags = translateTags(item.tags, lang);
    }
    
    content[item.id.toString()] = { question, answer, tags };
  });
  
  return content;
}

function updateLocaleFile(lang) {
  const filePath = path.join(localesDir, `${lang}.json`);
  
  try {
    const fileContent = fs.readFileSync(filePath, 'utf8');
    const json = JSON.parse(fileContent);
    
    if (!json.qna) {
      json.qna = {};
    }
    
    json.qna.content = generateContent(lang);
    
    fs.writeFileSync(filePath, JSON.stringify(json, null, 2), 'utf8');
    console.log(`Updated ${lang}.json successfully`);
  } catch (error) {
    console.error(`Error updating ${lang}.json:`, error.message);
  }
}

